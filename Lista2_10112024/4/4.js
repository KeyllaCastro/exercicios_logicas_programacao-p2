// Faça um Programa que verifique se uma letra digitada é vogal ou consoante

letra = prompt("Digite uma letra: ");

if (letra == "a" || letra == "e" || letra == "i" || letra == "o" || letra == "u") {
    alert("Esta letra é vogal.");
} else if (letra == "b" || letra == "c" || letra == "d" || letra == "f" || letra == "g" || 
           letra == "h" || letra == "j" || letra == "l" || letra == "m" || letra == "n" || 
           letra == "p" || letra == "q" || letra == "r" || letra == "s" || letra == "t" || 
           letra == "v" || letra == "w" || letra == "x" || letra == "y" || letra == "z") {
    alert("Esta letra é consoante.");
} 
